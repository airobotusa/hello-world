#include <stdio.h>

int main(void)
{
    int x = 17;
    int y = 50;
    
    printf("x is %i\n", x);
    printf("y is %i\n", y);
    
    // swapping...
    int temp = x;
    x = y;
    y = temp;
	// swapping is done
	// merge conflicts
	// merge made conflicts on purpose
 	// merge 2 for conflicts 222 333 444

    printf("x is now %i\n", x);
    printf("y is now %i\n", y);
}
